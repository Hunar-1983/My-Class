package src.day18_nestedLoops;

public class Nested_Loop1 {
    public static void main(String[] args) {

        for (int i = 0; i < 7; i++) {
            int a = 1;
            while (a <= 6) {

                System.out.print("*");
                a++;
            }
            System.out.println();

        }

    }
}
